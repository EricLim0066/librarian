class Me400:
    @staticmethod
    def main():
        # while基础循环语法
        # 如同if else判断一样,也属于布尔值(boolean)类型，如果条件为对，就一直执行，直到条件为错
        # 循环100次，输出语句
        time = 0
        while time <= 100:                              # while (循环条件)：
            print(f"{time}, I like you !")
            time += 1                                   # 条件必须+1设立终止条件，否则死循环

